public class Task1Loops {

    public static void main(String[] args) {

        int s = 4 ;

        while (s <= 11) {
            System.out.println(s);
            s++;
        }

        System.out.println(/***************/);

        for (int u = 4; u <= 11; u++) {
            System.out.println(u);

        }
    }
}
