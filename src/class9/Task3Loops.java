package class9;

// 3) 5 4 3 2 1

public class Task3Loops {
    public static void main(String[] args) {


        int d = 5;

        while (d > 0) {
            System.out.println(d);
            d--;
        }
        System.out.println(/***************/);

        for (int x = 5; x >= 0;  x--) {
            System.out.println(x);

        }
    }
}


